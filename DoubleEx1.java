package chapter2;

public class DoubleEx1 {
    /* 자바에서는 실수 자료형으로 기본 double형(8바이트)를 사용함. 그 외에, float향(4바아트) */
    public static void main(String[] args){
        double dnum = 3.14;
        float fnum = 3.14F; // flaat형으로 초기화하기 위해 식별자 F가 필요.

        System.out.println(dnum);
        System.out.println(fnum);
    }
}
